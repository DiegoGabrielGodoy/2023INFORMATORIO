#Escribe un programa que solicite al usuario dos números y luego imprima la suma, la resta, la multiplicación y la división de los dos números.
print("Por favor, ingrese los valores que desee para calcular la suma, resta, multiplicacion y división, gracias")
valor1=float(input("Ingrese el valor 1: "))
valor2=float(input("Ingrese el valor 2: "))
print("El valor de la suma es: ", (valor1+valor2))
print("El valor de la resta es: ", (valor1-valor2))
print("El valor de la multiplicación es: ", (valor1*valor2))
print("El valor de la disivión es: ", (valor1 / valor2))
